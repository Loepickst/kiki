    const appendMarkedText = (element, text, marks = []) => {
        const targets = marks.filter(Boolean);
        let cursor = 0;
        while (cursor < text.length) {
            let nextIndex = -1;
            let nextMark = "";
            targets.forEach((mark) => {
                const index = text.indexOf(mark, cursor);
                if (index !== -1 && (nextIndex === -1 || index < nextIndex)) {
                    nextIndex = index;
                    nextMark = mark;
                }
            });
            if (nextIndex === -1) {
                element.append(document.createTextNode(text.slice(cursor)));
                break;
            }
            if (nextIndex > cursor) {
                element.append(document.createTextNode(text.slice(cursor, nextIndex)));
            }
            const strong = document.createElement("b");
            strong.textContent = nextMark;
            element.append(strong);
            cursor = nextIndex + nextMark.length;
        }
    };

